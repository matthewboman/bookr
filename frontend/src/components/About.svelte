<div class="about">
    Book Your Own Tour makes it easy for bands to route tours. Our goal is to keep this free and public.
</div>
<div class="features">
    <p>
        You can check out <a href="https://github.com/matthewboman/bookr" target="_blank">our repository</a>to submit or help develop feature requests.
    </p>
    <p>Features we hope to implement include:</p>
    <ul>
        <li>
            Ability to add contacts
        </li>
        <li>
            Filtering venues by genres
        </li>
        <li>
            Ability to create an account to add private contacts, like promoters or friends who don't want their info public
        </li>
    </ul>
    <p>Features we might implement include:</p>
    <ul>
        <li>
            Venue rating and feedback
        </li>
        <li>
            Ability to create and export routing
        </li>
    </ul>
</div>
